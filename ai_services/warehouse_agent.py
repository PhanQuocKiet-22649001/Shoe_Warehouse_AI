app = FastAPI()
app.add_middleware(CORSMiddleware, allow_origins=["*"], allow_methods=["*"], allow_headers=["*"])

class ChatRequest(BaseModel):
    query: str

@app.post("/ask")
async def ask_warehouse(request: ChatRequest):
    try:
        print(f"\n--- USER QUERY: {request.query} ---")
        
        # 1. Tạo SQL và truy vấn lấy dữ liệu
        # Bước này Vanna làm rất tốt, cứ để nó làm.
        sql, df, fig = vn.ask(request.query, print_results=False)

        if not sql:
            return {"status": "error", "answer": "Tôi không hiểu câu hỏi này hoặc không tìm thấy bảng phù hợp."}

        # 2. Xử lý tóm tắt kết quả (Đoạn này hay bị lỗi dictionary)
        if df is not None and not df.empty:
            # Lấy 5 dòng đầu để AI tóm tắt cho nhanh, đỡ tốn VRAM
            data_context = df.to_dict('records')[:5]
            
            prompt = f"""
            Dựa trên kết quả truy vấn từ hệ thống kho giày: {data_context}
            Hãy trả lời câu hỏi: '{request.query}' một cách ngắn gọn, chuyên nghiệp bằng tiếng Việt.
            """
            
            try:
                # Cách 1: Thử dùng Vanna (nếu nó không lỗi)
                answer = vn.submit_prompt(prompt)
            except Exception:
                # Cách 2: Nếu Vanna lỗi 'dictionary', ta lấy kết quả thô từ dataframe luôn cho an toàn
                # Ví dụ: "Kết quả: 150"
                first_val = df.iloc[0, 0]
                answer = f"Theo dữ liệu trong kho, kết quả là: {first_val}"
        else:
            answer = "Hệ thống đã kiểm tra nhưng hiện tại không có dữ liệu nào khớp với yêu cầu của bồ."

        return {
            "status": "success",
            "sql": sql,
            "answer": answer.strip()
        }

    except Exception as e:
        print(f"[CRITICAL ERROR] {str(e)}")
        # Trả về câu trả lời tối thiểu nếu mọi thứ đều sập
        return {"status": "error", "answer": "Có lỗi khi xử lý dữ liệu. Bồ kiểm tra lại kết nối Database hoặc Ollama nhé."}

if __name__ == "__main__":
    import uvicorn
    uvicorn.run(app, host="0.0.0.0", port=8000)